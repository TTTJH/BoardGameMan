const I18nContext = React.createContext(null)

function format(template, values = {}) {
  return Object.entries(values).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, String(value)),
    template,
  )
}

function getInitialLanguage() {
  const saved = window.localStorage.getItem(LANGUAGE_STORAGE_KEY)
  return LANGUAGES.some((language) => language.code === saved) ? saved : 'en'
}

export function I18nProvider({ children }) {
  const [language, setLanguageState] = React.useState(getInitialLanguage)

  const setLanguage = React.useCallback((nextLanguage) => {
    const safeLanguage = LANGUAGES.some((item) => item.code === nextLanguage) ? nextLanguage : 'en'
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, safeLanguage)
    setLanguageState(safeLanguage)
  }, [])

  const t = React.useCallback((key, values) => {
    const template = translations[language]?.[key] ?? translations.en[key] ?? key
    return format(template, values)
  }, [language])

  const value = React.useMemo(() => ({
    language,
    setLanguage,
    t,
  }), [language, setLanguage, t])

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  const context = React.useContext(I18nContext)
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider')
  }
  return context
}

export function LanguageSwitcher() {
  const { language, setLanguage, t } = useI18n()

  return (
    <label className="language-switcher">
      <span className="sr-only">{t('language.label')}</span>
      <select
        value={language}
        onChange={(event) => setLanguage(event.target.value)}
        aria-label={t('language.label')}
      >
        {LANGUAGES.map((item) => (
          <option key={item.code} value={item.code}>
            {item.label}
          </option>
        ))}
      </select>
    </label>
  )
}
